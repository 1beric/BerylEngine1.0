package exceptions;

public class ComponentDNEException extends Exception {

	/**
	 * generated
	 */
	private static final long serialVersionUID = -8769794329574052311L;

	public ComponentDNEException() {
	}

	public ComponentDNEException(String message) {
		super(message);
	}

	public ComponentDNEException(Throwable cause) {
		super(cause);
	}

	public ComponentDNEException(String message, Throwable cause) {
		super(message, cause);
	}

	public ComponentDNEException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
